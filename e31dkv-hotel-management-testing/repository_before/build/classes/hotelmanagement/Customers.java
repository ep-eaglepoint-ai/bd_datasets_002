    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        d = (DefaultTableModel)rooms_table.getModel();
        int selectIndex = rooms_table.getSelectedRow();
        
            String selected = d.getValueAt(selectIndex , 0).toString();
            String customername = name.getText();
            String phone_input = phone.getText();
            String address_input = address.getText();
            String birthdate_input = birthdate.getDate().toString();
            String duration_input = duration.getText(); 
            

            try {
                pst = con.prepareStatement("insert into reservation(name, room, phone, address, birthdate, duration) values(?,?,?,?,?,?)");
                pst.setString(1,customername);
                pst.setString(2,selected);
                pst.setString(3,phone_input);
                pst.setString(4,address_input);
                pst.setString(5,birthdate_input);
                pst.setString(6,duration_input);
                pst.executeUpdate();
                pst = con.prepareStatement("update rooms set booked = 1 where id = ?");
                pst.setString(1, selected);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this , "Reservation is Sucessfull");
                
                phone.setText("");
                name.setText("");
                address.setText("");
                birthdate.setDate(null);
                duration.setText("");
                room_type.setSelectedIndex(0);
                
                Load_room("Any");


            } catch (SQLException ex) {
                Logger.getLogger(Rooms.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1MouseClicked

