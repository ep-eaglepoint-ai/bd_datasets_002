    public void Load_room(String type) {
        int c;
        try {
            if (type != "Any") {
                pst = con.prepareStatement("select * from rooms where room_type = ? and booked = 0");
                pst.setString(1, type);
            } else {
                pst = con.prepareStatement("select * from rooms where booked = 0");

            }
            ResultSet rs = pst.executeQuery();

            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();

            d = (DefaultTableModel) rooms_table.getModel();
            d.setRowCount(0);
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getInt("id"));
                    v2.add(rs.getString("floor"));
                    v2.add(rs.getString("room_type"));
                    v2.add(rs.getFloat("price"));
                }
                d.addRow(v2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Rooms.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

